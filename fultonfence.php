						<div class="spacer">&nbsp</div>
					<p>	The Fulton Fence is a hybrid art installation on a construction site in New York City.</br></br> 

						It consists of two components: an outdoor installation and a website. The installation transformed 120 meters of fencing around a construction site at Fulton Street & Broadway into a kinetic street sculpture. Signs with a URL and a 2D barcode connected pedestrians with the mobile website which mirrored the intervention online. This experience explored the idea of 'site' as generator and aggregator of information.</br></br>  

						By re-mixing local information, news, urban history, web widgets and mash-ups the project merges physical space into the web and vice-versa.</br></br> 
						The project was completed in November 2007 as a comission from the Downtown Alliance and the LMCC to improve the visual chaos created by construction sites in lower Manhattan.</p>			
