					<p>	The Urban Speaker is an art installation that transforms public space into an instant stage for mass communication. This portable urban furniture allows people to broadcast their voice in public by calling a telephone number from their mobile phones.</br></br>

						The project resembles construction signage and blends in with its urban surroundings. It consists of a tripod with an amplified loudspeaker, smartphone, battery and a traffic sign. The sign instructs passersby to dial a phone number to speak in public. Users who place the call get an automatic answer and can speak their mind for sixty seconds after which the call is terminated. A QR code also allows smartphones to quickly access a website for location, event and other details as well as quick dialing of the installation&#146;s phone.
